package Java8.project2;

import java.math.BigDecimal;

public class Developer {
	String name;
	int age;

	public Developer(String name,  int age) {
		super();
		this.name = name;
		this.age = age;
	}
	public Developer() {
	}
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}



	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
	
	
}
